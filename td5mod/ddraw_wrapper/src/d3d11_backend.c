/* ------------------------------------------------------------------------
 * Photo-booth frame capture: copy the final composited backbuffer to CPU.
 * Used by the offline car-preview generator (td5_game.c). A capture is
 * requested for the next presented frame; the BGRA pixels are then read with
 * Backend_GetCapture. ------------------------------------------------------ */
static int            s_capture_request = 0;
static unsigned char *s_capture_pixels  = NULL;   /* BGRA, w*h*4 */
static int            s_capture_w = 0, s_capture_h = 0;
static int            s_capture_ready = 0;

void Backend_RequestCapture(void) { s_capture_request = 1; s_capture_ready = 0; }
/* Called from every present path (before Present, while the composited frame is
 * still in swap-chain buffer 0) to service a pending capture request. */
void Backend_CaptureIfRequested(void); /* fwd (impl after backend_do_capture) */
int  Backend_GetCapture(unsigned char **px, int *w, int *h)
{
    if (!s_capture_ready || !s_capture_pixels) return 0;
    if (px) *px = s_capture_pixels;
    if (w)  *w  = s_capture_w;
    if (h)  *h  = s_capture_h;
    return 1;
}
static void backend_do_capture(void)
{
    ID3D11Texture2D *bb = NULL;
    HRESULT hr = IDXGISwapChain_GetBuffer(g_backend.swap_chain, 0,
                                          &IID_ID3D11Texture2D, (void **)&bb);
    if (FAILED(hr) || !bb) return;
    D3D11_TEXTURE2D_DESC desc;
    ID3D11Texture2D_GetDesc(bb, &desc);
    D3D11_TEXTURE2D_DESC sd = desc;
    sd.Usage = D3D11_USAGE_STAGING;
    sd.BindFlags = 0;
    sd.CPUAccessFlags = D3D11_CPU_ACCESS_READ;
    sd.MiscFlags = 0;
    ID3D11Texture2D *st = NULL;
    hr = ID3D11Device_CreateTexture2D(g_backend.device, &sd, NULL, &st);
    if (FAILED(hr)) { ID3D11Texture2D_Release(bb); return; }
    ID3D11DeviceContext_CopyResource(g_backend.context, (ID3D11Resource *)st,
                                     (ID3D11Resource *)bb);
    D3D11_MAPPED_SUBRESOURCE m;
    hr = ID3D11DeviceContext_Map(g_backend.context, (ID3D11Resource *)st, 0,
                                 D3D11_MAP_READ, 0, &m);
    if (SUCCEEDED(hr)) {
        int w = (int)desc.Width, h = (int)desc.Height;
        unsigned char *buf = (unsigned char *)malloc((size_t)w * h * 4);
        if (buf) {
            for (int y = 0; y < h; y++)
                memcpy(buf + (size_t)y * w * 4,
                       (unsigned char *)m.pData + (size_t)y * m.RowPitch, (size_t)w * 4);
            free(s_capture_pixels);
            s_capture_pixels = buf;
            s_capture_w = w; s_capture_h = h; s_capture_ready = 1;
        }
        ID3D11DeviceContext_Unmap(g_backend.context, (ID3D11Resource *)st, 0);
    }
    ID3D11Texture2D_Release(st);
    ID3D11Texture2D_Release(bb);
}
void Backend_CaptureIfRequested(void)
{
    if (s_capture_request) { backend_do_capture(); s_capture_request = 0; }
}

